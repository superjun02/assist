package com.assist.pattern.model;

public class Pattern {
	private Integer etc = 0;
	private Integer food = 0;
	private Integer shopping = 0;
	private Integer educate = 0;
	private Integer life = 0;
	private Integer leisure = 0;
	private Integer culture = 0;
	private Integer salary = 0;
	
	public Integer getEtc() {
		return etc;
	}
	public void setEtc(Integer etc) {
		if (etc != null) {
			this.etc = etc;
		}
	}
	public Integer getFood() {
		return food;
	}
	public void setFood(Integer food) {
		if (food != null) {
			this.food = food;
		}
	}
	public Integer getShopping() {
		return shopping;
	}
	public void setShopping(Integer shopping) {
		if (shopping != null) {
			this.shopping = shopping;
		}
	}
	public Integer getEducate() {
		return educate;
	}
	public void setEducate(Integer educate) {
		if (educate != null) {
			this.educate = educate;
		}
	}
	public Integer getLife() {
		return life;
	}
	public void setLife(Integer life) {
		if (life != null) {
			this.life = life;
		}
	}
	public Integer getLeisure() {
		return leisure;
	}
	public void setLeisure(Integer leisure) {
		if (leisure != null) {
			this.leisure = leisure;
		}
	}
	public Integer getCulture() {
		return culture;
	}
	public void setCulture(Integer culture) {
		if (culture != null) {
			this.culture = culture;
		}
	}
	public Integer getSalary() {
		return salary;
	}
	public void setSalary(Integer salary) {
		if (salary != null) {
			this.salary = salary;
		}
	}
	
	
	
}
